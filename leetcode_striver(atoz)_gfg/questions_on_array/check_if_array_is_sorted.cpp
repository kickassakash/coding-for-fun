class Solution {
  public:
    bool arraySortedOrNot(int arr[], int n) {
        bool ans = true;
        for(int i =0;i<n-1;i++){
            if(arr[i] <=arr[i+1]){
                ans = true;
            }
            else{
                ans = false;
                break;
            }
        }
        return ans;
    }
};

